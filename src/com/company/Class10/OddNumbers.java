public class OddNumbers {
    public static void main(String[] args) {
        System.out.println("These are odd numbers from 1 to 100");
        for(int a = 1; a <=100; a++) {
            if (a%2 != 0) {
                System.out.println(a + "");
            }

        }

    }
}
